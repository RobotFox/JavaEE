package esercizi;

public class Record {

	String sport, campione;
	double valoreRecord;

	public String getSport() {
		return sport;
	}

	public void setSport(String sport) {
		this.sport = sport;
	}

	public String getCampione() {
		return campione;
	}

	public void setCampione(String campione) {
		this.campione = campione;
	}

	public double getValoreRecord() {
		return valoreRecord;
	}

	public void setValoreRecord(double valoreRecord) {
		this.valoreRecord = valoreRecord;
	}

}
